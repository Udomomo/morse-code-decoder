export function getReverseMorseCodeTable(table: MorseCodeTable): MorseCodeTable {
  const reverseTable: MorseCodeTable = {};
  for (const [char, code] of Object.entries(table)) {
    reverseTable[code] = char;
  }
  return reverseTable;
}